public class VariousVariables {
  public static void main(String[] args) {
    int numChickens = 9000;
    double amountBacon = 0.1;
    String cowName = "Moomoo";

    System.out.println("Chicken:\n" + numChickens + "\nBacon (kg):\n" + amountBacon + "\nCow:\n" + cowName);

    System.out.println("\nAnd finally, a summary:");
    System.out.println(numChickens);
    System.out.println(amountBacon);
    System.out.println(cowName);
  }
}
